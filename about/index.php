 <b>Социальное партнерство</b> 
<br />
 
<br />
 Шатурское местное отделение ВОГ сотрудничает с Администрацией муниципального района, Департаментом социальной защиты населения Шатурского района, ФСС по Московской области и учреждениями в области социальной реабилитации и интеграции инвалидов по слуху, а также с фирмой &quot;Юдипи&quot;,которая обеспечивает общество информационными технологиями (доступ к Интернет) и фирма &quot;Шатура плюс&quot; (РТВ &quot;Подмосковье&quot;) обеспечила подключение кабельного телевидения. 
