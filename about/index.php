<br />
 Целью работы Шатурского местного отделения ВОГ (Шатурское МО ВОГ) является повышение качества и комфорта жизнедеятельности глухих, имеющих проблемы со слухом и речью, предоставление им равных с другими гражданами Российской Федерации прав и возможностей. 
